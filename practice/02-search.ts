/**
 * Search
 * `cmd+f`
 *
 * Replace (also works when already searching)
 * `cmd+shift+f`
 */

/**
 * Search next
 * `enter` or `cmd+g` (also works outside of search)
 * Search previous
 * `shift+enter` or `cmd+shift+g` (also works outside of search)
 */

/**
 * Go through search history
 * `up/down`
 */

/**
 * Toggle search mode
 * case:  `cmd+opt+c`
 * regex: `cmd+opt+r`
 * word:  `cmd+opt+w`
 */
