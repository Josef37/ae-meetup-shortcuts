/**
 * Close editor
 * `cmd+w`
 *
 * New editor
 * `cmd+n`
 *
 * New window
 * `cmd+shift+n`
 */

/**
 * Split editor
 * `cmd+\`
 *
 * Move editor
 * `cmd+ctrl+left/right`
 */

/**
 * Focus on editor group
 * `cmd+1/2/3/...`
 * `cmd+k cmd+left/right`
 */

/**
 * Focus next editor
 * `cmd+opt+left/right`
 */

/**
 * Move editor group
 * `cmd+k left/right`
 */
